import math

def soma(x, y):
    calculo = x + y
    return calculo

def subtracao(x, y):
    calculo = x - y
    return calculo

def multiplicacao(x, y):
    calculo = x * y
    return round(calculo, 2)

def divisao(x, y):
    calculo = x / y
    return round(calculo, 2)

def exponenciacao(x, y):
    calculo = x ** y
    return calculo

def raizQuadrada(x):
    calculo = math.sqrt(x)
    return calculo

def logaritmo(x):
    calculo = math.log10(x)
    return calculo
    